'''
Python = Linguagem de programação.
Tipos de tipagem: Dinâmica / Forte.
str -> String = cadeia de caracteres (Texto).

    - Strings são textos que estão contidos entre aspas " ".
'''

print(1234)

# Aspas simples
print('Luis Otávio')

# Aspas duplas
print("Luis Otávio")

# Aspas duplas dentro de Aspas simples
print(1, 'Luis "Otávio"')

# Aspas simples dentro de Aspas duplas
print(2, "Luis 'Otávio'")

# Caracter de Escape
print("Luis \"Otávio\"")

# r
print(r"Luis \"Otávio\"")
